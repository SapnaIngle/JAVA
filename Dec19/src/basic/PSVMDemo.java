package basic;

public class PSVMDemo {

    static public void main(String[] args) {                  //--This is called as Method signature

        System.out.println(args[0]);


    }

    // public :- access specifier or modifier.
    // static :- keyword, without creating object of class JVM is accessing main function.
    // public give access , JVM runs the main function.
    // void :- main function is return nothing, void means nothing.
    // main :- it is identifier.
    // String[} :- command line argument for the main function.
    // args :- variable name.


}
